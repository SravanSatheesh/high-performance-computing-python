import numpy as np

# 1. Create a 10x10 matrix with random numbers between 0 and 100
matrix = np.random.uniform(0, 100, (10, 10))
print("Original 10x10 matrix:\n", matrix)

# 2. Calculate the mean value of each row
row_means = matrix.mean(axis=1)
print("Mean of each row:\n", row_means)

# 3. Calculate the maximum of each column
col_max = matrix.max(axis=0)
print("Maximum of each column:\n", col_max)

# 4. Calculate total standard deviation
total_std = matrix.std()
print("Total standard deviation:", total_std)

# 5. Find the index of the largest number in the entire array
max_index = np.argmax(matrix)
print("Index of largest number in flattened array:", max_index)

