import numpy as np
import matplotlib.pyplot as plt

# ----------------------------
# Task 4.1: Creating NumPy arrays
# ----------------------------
print("\n--- Task 4.1: Creating NumPy arrays ---")
# 1D array from 0 to 9
arr_1d = np.arange(10)
print("1D array:", arr_1d)

# 3x3 random matrix
matrix_3x3 = np.random.rand(3, 3)
print("3x3 random matrix:\n", matrix_3x3)

# Replace all even numbers in array with -1
arr_even_replaced = arr_1d.copy()
arr_even_replaced[arr_even_replaced % 2 == 0] = -1
print("Array with even numbers replaced:", arr_even_replaced)

# ----------------------------
# Task 4.2: Scalar product of two vectors
# ----------------------------
print("\n--- Task 4.2: Scalar product ---")
v1 = np.array([5, 3, 2])
v2 = np.array([1, 2, 3])

# Without NumPy
scalar_manual = sum([v1[i]*v2[i] for i in range(3)])
print("Scalar product without NumPy:", scalar_manual)

# With NumPy
scalar_numpy = np.dot(v1, v2)
print("Scalar product with NumPy:", scalar_numpy)

# ----------------------------
# Task 4.3: Matrix multiplication
# ----------------------------
print("\n--- Task 4.3: Matrix multiplication ---")
A = np.random.rand(3, 3)
B = np.random.rand(3, 3)

# Using for loops
C_manual = np.zeros((3, 3))
for i in range(3):
    for j in range(3):
        for k in range(3):
            C_manual[i, j] += A[i, k] * B[k, j]

print("Matrix multiplication with loops:\n", C_manual)

# Using NumPy
C_numpy = np.dot(A, B)
print("Matrix multiplication with np.dot():\n", C_numpy)

# ----------------------------
# Task 4.4: Moving average
# ----------------------------
print("\n--- Task 4.4: Moving average ---")
arr_large = np.random.rand(10)  # example array, can be larger
k = 3
moving_avg = np.convolve(arr_large, np.ones(k)/k, mode='valid')
print("Array:", arr_large)
print(f"Moving average (window={k}):", moving_avg)

# ----------------------------
# Task 4.5: 2D Random Walk
# ----------------------------
print("\n--- Task 4.5: 2D Random Walk ---")
steps = np.random.randint(0, 4, 1000)

dx = np.where((steps == 2), -1, np.where((steps == 3), 1, 0))
dy = np.where((steps == 0), 1, np.where((steps == 1), -1, 0))

x = np.cumsum(dx)
y = np.cumsum(dy)

plt.figure(figsize=(6, 6))
plt.plot(x, y, marker='o', markersize=2)
plt.title("2D Random Walk")
plt.xlabel("X")
plt.ylabel("Y")
plt.show()

